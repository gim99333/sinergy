# Получаем стороны прямоугольника от пользователя
try:
    a = float(input("Введите длину первой стороны прямоугольника: "))
    b = float(input("Введите длину второй стороны прямоугольника: "))
    
    # Проверяем, что стороны положительные
    if a <= 0 or b <= 0:
        print("Ошибка: стороны должны быть положительными числами")
    else:
        # Вычисляем площадь и периметр
        area = a * b
        perimeter = 2 * (a + b)
        
        # Выводим результаты c округлением до знака после запятой
        print(f"Площадь прямоугольника: {area:.1f}")
        print(f"Периметр прямоугольника: {perimeter:.1f}")
        
except ValueError:
    print("Ошибка: введите числовые значения для сторон")
    
